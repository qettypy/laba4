#include <iostream>

using namespace std;

int main()
{
  setlocale(LC_ALL, "");

    int choice;
    double value, result;

    cout << "Выберите единицу измерения для перевода:\n";
    cout << "1. Сажени в метры\n";
    cout << "2. Нз дюймов в сантиметры\n";
    cout << "3. Футов в метры\n";
    cout << "4. Драхм в граммы\n";
    cout << "5. Унций в граммы\n";
    cout << "6. Фунтов в килограммы\n";
    cout << "7. Аршинов в метры\n";
    cout << "8. Золотников в граммы\n";
    cout << "9. Дюймов в миллиметры\n";

    cout << "Введите номер выбранной единицы измерения: ";
    cin >> choice;

    cout << "Введите значение для перевода: ";
    cin >> value;

    switch (choice) {
    case 1:
        result = value * 2.1366;
        cout << "Результат: " << result << " метров\n";
        break;
    case 2:
        result = value * 2.54;  // 1 дюйм = 2.54 см
        cout << "Результат: " << result << " сантиметров\n";
        break;
    case 3:
        result = value * 0.3048;
        cout << "Результат: " << result << " метров\n";
        break;
    case 4:
        result = value * 3.7325;
        cout << "Результат: " << result << " граммов\n";
        break;
    case 5:
        result = value * 29.86;
        cout << "Результат: " << result << " граммов\n";
        break;
    case 6:
        result = value * 0.40951;
        cout << "Результат: " << result << " килограммов\n";
        break;
    case 7:
        result = value * 0.7112;
        cout << "Результат: " << result << " метров\n";
        break;
    case 8:
        result = value * 4.2657;
        cout << "Результат: " << result << " граммов\n";
        break;
    case 9:
        result = value * 25.3995;
        cout << "Результат: " << result << " миллиметров\n";
        break;
    default:
        cout << "Ошибка: неверный выбор единицы измерения.\n";
        break;
    }

    return 0;
}
